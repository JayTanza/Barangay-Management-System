    private void jButton51ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton51ActionPerformed
        // TODO add your handling code here:
        //INSERT INTO `requestdoc`(`Firstname`, `Lastname`, `Address`, `Doc`, `Reason`) VALUES (?,?,?,?,?)
         String registerUserQuery = "INSERT INTO `requestdoc`(`Firstname`, `Lastname`, `Address`, `Doc`, `Reason`) VALUES (?,?,?,?,?)";
         try{
                          //InputStream is = new FileInputStream(new File(s));
                          pst = (PreparedStatement) MyConnection.getConnection().prepareStatement(registerUserQuery);
                          pst.setString(1,jTextField_Firstname.getText());
                          pst.setString(2,jTextField_Firstname1.getText());
                          pst.setString(3,jTextField_Firstname3.getText());
                          pst.setString(4,jComboBox_Documents1.getSelectedItem().toString());
                          pst.setString(5,jComboBox_Documents.getSelectedItem().toString());
                          pst.executeUpdate();
    }//GEN-LAST:event_jButton51ActionPerformed

        catch (SQLException ex) {
            Logger.getLogger(ReqDoc.class.getName()).log(Level.SEVERE, null, ex);
        }
         JOptionPane.showMessageDialog(null, "Request Successfully Registered","Barangay System",JOptionPane.INFORMATION_MESSAGE);
        
    }
